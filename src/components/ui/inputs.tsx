"use client";
import * as React from "react";
import { cn } from "@/lib/utils";

export type InputProps = React.InputHTMLAttributes<HTMLInputElement>;

const Input = React.forwardRef<HTMLInputElement, InputProps>(
  ({ className, type, ...props }, ref) => {
    return (
      <div
        className="group/input rounded-lg p-[2px] transition duration-300"
      >
        <input
          type={type}
          className={cn(
            `shadow-input dark:placeholder-text-neutral-600 flex h-10 w-full rounded-md border-none
             bg-gray-500 px-3 py-2 text-sm text-black transition duration-400 group-hover/input:shadow-none
              file:border-0 file:bg-transparent file:text-sm file:font-medium placeholder:text-neutral-400
               focus-visible:ring-[2px] focus-visible:ring-neutral-400 focus-visible:outline-none 
               disabled:cursor-not-allowed disabled:opacity-50 dark:bg-zinc-800 dark:text-gray-400
              dark:shadow-[0px_0px_1px_1px_#404040] dark:focus-visible:ring-neutral-600`,
            className
          )}
          ref={ref}
          {...props}
        />
      </div>
    );
  }
);
Input.displayName = "Input";


